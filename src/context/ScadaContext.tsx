import { createContext, useContext, useState, ReactNode } from "react";

export type ScadaType = "solar_pump" | "mini_grid" | "rooftop_solar" | "solar_water_heater" | "solar_street_light" | "solar_high_mast";

export interface ScadaDevice {
  id: string;
  name: string;
  type: ScadaType;
  location: string;
  district: string;
  capacity: number; // kW
  status: "online" | "offline" | "maintenance";
  installationDate: string;
  beneficiaryName: string;
  beneficiaryContact: string;
  latitude: number;
  longitude: number;
  imei: string;
  // Live data (simulated)
  currentPower: number;
  todayEnergy: number;
  totalEnergy: number;
  lastSync: string;
  // Additional fields for specific types
  temperature?: number; // For water heaters
  operatingHours?: number; // For street lights/high masts
  waterFlow?: number; // For water heaters (L/min)
  brightness?: number; // For street lights (lux)
  coverageArea?: string; // For high masts
}

interface ScadaContextType {
  devices: ScadaDevice[];
  addDevice: (device: Omit<ScadaDevice, "id" | "currentPower" | "todayEnergy" | "totalEnergy" | "lastSync">) => void;
  updateDevice: (id: string, device: Partial<ScadaDevice>) => void;
  deleteDevice: (id: string) => void;
  getDeviceById: (id: string) => ScadaDevice | undefined;
}

